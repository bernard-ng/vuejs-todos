<template>
    <section class="todoapp">
        <header class="header">
            <h1>Todos</h1>
            <input type="text" class="new-todo" placeholder="add a todo" v-model="newTodo" @keyup.enter="addTodo">
        </header>
        <div class="main">
            <ul class="todo-list">
                <li class="todo" :class="{completed: todo.completed}"  v-for="todo in todos">
                    <div class="view">
                        <input type="checkbox" v-model="todo.completed" class="toggle">
                        <label>{{ todo.name }}</label>
                    </div>
                </li>
            </ul>
        </div>
        <footer class="footer">
            <span class="todo-count"><strong>{{ remaing }}</strong> items left</span>
        </footer>
    </section>
</template>

<script>
export default {
    data() {
        return {
            todos: [],
            newTodo: '',
        }
    },

    methods: {
        addTodo() {
            this.todos.push({
                completed: false,
                name: this.newTodo
            })

            this.newTodo = ''
        }
    },

    computed: {
        remaing() {
            return this.todos.filter(todo => !todo.completed).length
        }
    }
}
</script>

